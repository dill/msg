   object$term<-object$msg$term
   object$dim<-object$msg$dim

   #### MAGIC HAPPENS HERE!!!!

   if(!is.null(object$xt$bnd)){
      bnd<-object$xt$bnd
   }else{
      bnd<-NULL
   }

   mds.obj<-object$msg$mds.obj
   my.grid<-object$msg$grid
